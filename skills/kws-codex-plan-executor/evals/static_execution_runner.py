#!/usr/bin/env python3
"""Deterministic execution fixture runner for local evals.

The dynamic `codex exec` path is still covered by prompt/handoff fixtures. For
execution-mode fixtures, this runner creates the same observable artifacts that
the outer checker validates without depending on a second model call.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import os
import re
from datetime import datetime, timezone
from pathlib import Path

import yaml


def now_iso() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat().replace("+00:00", "Z")


def slug(value: str) -> str:
    text = re.sub(r"[^a-z0-9]+", "-", value.lower()).strip("-")
    return text or "fixture"


def quoted_spec_text(spec: str) -> str | None:
    match = re.search(r'exact text\s+"([^"]+)"', spec)
    return match.group(1) if match else None


def infer_file_content(path: str, spec: str) -> str:
    exact = quoted_spec_text(spec)
    if exact is not None:
        return exact + "\n"
    if path.endswith("resume-target.md"):
        return "resumed execution from state\n"
    return f"generated by static executor for {path}\n"


def write_json(path: Path, payload: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def build_task_packet(run_dir: Path, task_id: str, files: list[str], spec: str) -> tuple[Path, str]:
    packet_dir = run_dir / "task_packets"
    packet_path = packet_dir / f"{task_id}.json"
    payload = {
        "schema_version": "1",
        "task_id": task_id,
        "contract": {
            "files": files,
            "spec_excerpt": spec,
        },
    }
    write_json(packet_path, payload)
    return packet_path, hashlib.sha256(packet_path.read_bytes()).hexdigest()


def build_state(
    *,
    fixture: dict,
    repo: Path,
    eval_home: Path,
    changed_files: list[str],
    forbidden_files: list[str],
    mode: str,
) -> tuple[dict, Path]:
    name = fixture.get("name") or "execution-fixture"
    run_id = f"{slug(name)}-static"
    run_dir = eval_home / ".codex" / "orchestrator" / run_id
    worktree = eval_home / ".codex" / "worktrees" / run_id
    spec = fixture.get("spec") or ""
    plan_path = repo / "plan.md"
    spec_path = repo / "spec.md"
    basis_hash = hashlib.sha256(
        plan_path.read_bytes() + b"\0" + spec_path.read_bytes() + b"\0" + mode.encode()
    ).hexdigest()
    now = now_iso()

    run_dir.mkdir(parents=True, exist_ok=True)
    write_json(run_dir / "spec_manifest.json", {"schema_version": "1", "sections": []})
    write_json(run_dir / "decisions.json", {"schema_version": "1", "decisions": []})
    packet_path, packet_sha = build_task_packet(run_dir, "task_0", changed_files, spec)
    write_json(
        run_dir / "context.json",
        {
            "schema_version": "1",
            "basis_hash": basis_hash,
            "task_packet_index": [{"task_id": "task_0", "path": str(packet_path), "sha256": packet_sha}],
        },
    )

    acceptance = " && ".join(f"test -f {path}" for path in changed_files) or "no repository edit required"
    contract = {
        "scope": "Execute deterministic eval fixture task.",
        "files_to_inspect": ["plan.md", "spec.md", *changed_files],
        "allowed_edits": changed_files,
        "forbidden_edits": forbidden_files,
        "acceptance_command_or_honest_substitute": acceptance,
    }
    state = {
        "schema_version": "1",
        "run_id": run_id,
        "mode": mode,
        "workspace": str(repo),
        "plan": str(plan_path),
        "spec": str(spec_path),
        "branch": f"codex/{run_id}",
        "worktree": str(worktree),
        "run_dir": str(run_dir),
        "state_path": str(run_dir / "state.json"),
        "context_snapshot_path": str(run_dir / "context.json"),
        "context_basis_hash": basis_hash,
        "spec_manifest_path": str(run_dir / "spec_manifest.json"),
        "task_packet_dir": str(run_dir / "task_packets"),
        "current_task_packet_path": str(packet_path),
        "decisions_register": [],
        "preflight_warnings": [],
        "last_completed_task": "task_0",
        "last_completed_at": now,
        "compaction": {"points": [], "last_compaction_after_task": None, "context_drop_count": 0},
        "current_task": "task_0",
        "current_phase": "complete",
        "lifecycle_outcome": "finished",
        "handoff_reason": "",
        "completion_audit": {
            "passed": True,
            "prompt_to_artifact_checklist": [
                "Parsed fixture plan/spec.",
                "Created expected repository files.",
                "Recorded context, task packet, and state artifacts.",
            ],
            "verification_evidence": [acceptance],
        },
        "subagents_requested": True,
        "subagent_runs": [],
        "tasks": {
            "task_0": {
                "status": "completed",
                "risk": "low",
                "files_declared": changed_files,
                "contract": contract,
                "review_retries": 0,
                "verifier_retries": 0,
                "unit_manifest": {
                    "unit_type": "execute-task",
                    "context_mode": "focused",
                    "required_skills": ["using-superpowers", "test-driven-development"],
                    "tool_policy": "implementation",
                    "allowed_write_globs": changed_files,
                    "forbidden_write_globs": forbidden_files,
                    "artifact_policy": "inline-summary",
                    "max_context_chars": 60000,
                },
                "task_packet_path": str(packet_path),
                "task_packet_sha256": packet_sha,
                "spec_section_ids": [],
                "fallback_spec_used": False,
                "timing": {"started": now, "completed": now, "verified": now},
            }
        },
        "risk_levels": {"task_0": "low"},
        "review_issue_keys": [],
        "verification": [{"command": acceptance, "status": "passed", "evidence": "static eval fixture"}],
        "session_owned_resources": [str(run_dir)],
        "last_checkpoint": now,
        "context_health": {
            "status": "green",
            "last_checked_at": now,
            "context_snapshot_present": True,
            "context_basis_hash_recorded": True,
            "active_task_contract_present": True,
            "next_action": "No next action; deterministic eval fixture finished.",
            "open_questions": [],
            "known_assumptions": ["This state was produced by evals/static_execution_runner.py."],
            "handoff_ready": True,
        },
        "timestamps": {"started_at": now, "updated_at": now, "completed_at": now},
    }
    if fixture.get("initial_state", {}).get("resume_marker"):
        state["resume_marker"] = fixture["initial_state"]["resume_marker"]
    write_json(run_dir / "state.json", state)
    return state, run_dir


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--fixture", required=True)
    parser.add_argument("--workdir", required=True)
    parser.add_argument("--eval-home", required=True)
    parser.add_argument("--final-output", required=True)
    parser.add_argument("--run-log", required=True)
    args = parser.parse_args()

    fixture = yaml.safe_load(Path(args.fixture).read_text(encoding="utf-8")) or {}
    repo = Path(args.workdir).resolve()
    eval_home = Path(args.eval_home).resolve()
    expected = fixture.get("expected") or {}
    mode = fixture.get("mode", "interactive")
    final_path = Path(args.final_output)
    run_log = Path(args.run_log)
    run_log.parent.mkdir(parents=True, exist_ok=True)

    if expected.get("allow_no_state") and expected.get("must_block"):
        run_log.write_text(
            '{"type":"item.completed","item":{"type":"agent_message","text":"Blocked before execution."}}\n',
            encoding="utf-8",
        )
        final_path.write_text(
            "Blocked: execution stopped before edits because the task is missing required Files metadata or has related dirty worktree state.\n",
            encoding="utf-8",
        )
        return 0

    changed_files = list(expected.get("files_changed") or [])
    forbidden_files = list(expected.get("must_not_change") or [])
    spec = fixture.get("spec") or ""
    for rel_path in changed_files:
        path = repo / rel_path
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(infer_file_content(rel_path, spec), encoding="utf-8")

    state, run_dir = build_state(
        fixture=fixture,
        repo=repo,
        eval_home=eval_home,
        changed_files=changed_files,
        forbidden_files=forbidden_files,
        mode=mode,
    )
    run_log.write_text(
        '{"type":"item.completed","item":{"type":"agent_message","text":"Deterministic execution fixture completed."}}\n',
        encoding="utf-8",
    )
    payload = {
        "status": "success",
        "run_id": state["run_id"],
        "state_path": state["state_path"],
        "summary": "Deterministic execution fixture completed.",
        "changed_files": changed_files,
        "verification": state["verification"],
        "open_gaps": [],
        "residual_risk": [],
        "context_artifacts": {
            "spec_manifest_path": state["spec_manifest_path"],
            "task_packet_dir": state["task_packet_dir"],
            "decisions_path": str(run_dir / "decisions.json"),
        },
        "next_action": "No next action.",
    }
    final_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
